const quote = (identifier: string) => `"${identifier.replaceAll('"', '""')}"`;

export function postgresSql(workspace: Workspace): string {
  const blocks = workspace.tables.map((table) => {
    const lines = table.columns.map((field) => {
      const notNull = field.primaryKey || !field.nullable ? " NOT NULL" : "";
      return `  ${quote(field.name || "unnamed_column")} ${field.type || "text"}${notNull}`;
    });

    const primaryKeys = table.columns.filter((field) => field.primaryKey);
    if (primaryKeys.length) {
      lines.push(`  PRIMARY KEY (${primaryKeys.map((field) => quote(field.name || "unnamed_column")).join(", ")})`);
    }

    workspace.relations
      .filter((relation) => relation.source === table.id)
      .forEach((relation) => {
        const sourceColumn = table.columns.find((field) => field.id === relation.sourceColumnId);
        const targetTable = workspace.tables.find((item) => item.id === relation.target);
        const targetColumn = targetTable?.columns.find((field) => field.id === relation.targetColumnId);
        if (sourceColumn && targetTable && targetColumn) {
          lines.push(
            `  FOREIGN KEY (${quote(sourceColumn.name || "unnamed_column")}) REFERENCES ${quote(targetTable.name || "unnamed_table")} (${quote(targetColumn.name || "unnamed_column")})`,
          );
        }
      });

    return `CREATE TABLE ${quote(table.name || "unnamed_table")} (\n${lines.join(",\n")}\n);`;
  });

  return `-- Generated by SchemaForge\n-- PostgreSQL schema: ${workspace.name}\n\n${blocks.join("\n\n")}`;
}

export function validateWorkspace(workspace: Workspace): string[] {
  const messages: string[] = [];
  const seenTables = new Set<string>();
  workspace.tables.forEach((table) => {
    const normalized = table.name.trim().toLowerCase();
    if (!normalized) messages.push("Every table needs a name.");
    if (seenTables.has(normalized)) messages.push(`Table name “${table.name}” is duplicated.`);
    seenTables.add(normalized);

    const seenColumns = new Set<string>();
    table.columns.forEach((field) => {
      const columnName = field.name.trim().toLowerCase();
      if (!columnName) messages.push(`${table.name || "A table"} has an unnamed column.`);
      if (seenColumns.has(columnName)) messages.push(`Column “${field.name}” is duplicated in ${table.name}.`);
      seenColumns.add(columnName);
    });
    if (!table.columns.length) messages.push(`${table.name || "A table"} has no columns.`);
  });

  workspace.relations.forEach((relation) => {
    const targetTable = workspace.tables.find((table) => table.id === relation.target);
    const targetColumn = targetTable?.columns.find((field) => field.id === relation.targetColumnId);
    if (!targetColumn) messages.push("A relationship points to a deleted column.");
    else if (!targetColumn.primaryKey) messages.push(`${targetTable?.name}.${targetColumn.name} should be a primary key before it is referenced.`);
  });
  return [...new Set(messages)];
}
