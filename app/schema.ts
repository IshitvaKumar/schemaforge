export type Column = {
  id: string;
  name: string;
  type: string;
  primaryKey: boolean;
  nullable: boolean;
};

export type SchemaTable = {
  id: string;
  name: string;
  position: { x: number; y: number };
  columns: Column[];
};

export type Relation = {
  id: string;
  source: string;
  sourceColumnId: string;
  target: string;
  targetColumnId: string;
};

export type Workspace = {
  name: string;
  tables: SchemaTable[];
  relations: Relation[];
};

const column = (id: string, name: string, type: string, primaryKey = false, nullable = false): Column => ({
  id,
  name,
  type,
  primaryKey,
  nullable,
});

